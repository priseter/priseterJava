package java01_variables;

public class Variables_02 {
	public static void main(String[] args) {
		
		int data; // int타입 변수 선언 공간 (Declartion)
		// -> int형 데이터를 저장할 수 있는 메모리 공간이 생성된다.
		
		data = 17; // =은 같다라는 뜻이 아니라 오른쪽 값을 왼쪽 값에 대입한다는 뜻, 같다는 ==이다.
		
		data = 88; // 값 대입 기존의 값을 지우고 대입하게 된다.
		// 지금은 17이 있던 값을 지우고 88을 저장
		
		System.out.println( data );
		// data 변수에 저장된 88이 불러와서 출력이 된다.
		
		int num = 123;
		
	}

}
