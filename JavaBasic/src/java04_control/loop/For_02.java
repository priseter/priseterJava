package java04_control.loop;

public class For_02 {

	public static void main(String[] args) {

		//반복문에서는 int i 변수를 많이 사용한다.
		//  **iterate(반복한다)의 첫 글자
		for(int i = 0; i < 10 ;i++) {
			System.out.println(i);
			
		}
		// i변수가 0부터 10보다 작은 동안에 1씩 증가하면서 반복한다
		// 반복 횟수 : 10번
		// 반복구간: 0~9까지
		;
		for(int i = 7;i<13;i++) {
			System.out.println(i);
			}
	}

}
