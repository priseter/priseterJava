package java04_control.loop;

public class For_04 {

	public static void main(String[] args) {

		//반복횟수 : 7
		//반복구간 : 0~6
		
		//반복하면서 수행할 작업
		//초기식 : int i = 0
		//조건식 : i < 7
		//증감식 : i++
		
		for (int i=0;i<7;i++) {
			//"Hello" 출력
			System.out.println("Hello");
		}
	}

}
